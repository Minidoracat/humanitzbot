#!/usr/bin/env node
'use strict';

/**
 * Build humanitz-template.db — pre-seeded SQLite database with all game
 * reference data (718 items, 122 buildings, 154 recipes, etc.).
 *
 * This is a dev-only build tool. The template DB is committed to git and
 * shipped with releases. On first run, database.js copies it as the starting
 * point for a user's humanitz.db, eliminating the need for game-tables-raw.json
 * at runtime.
 *
 * Usage:
 *   node scripts/build-template-db.js
 *   npm run build:template
 *
 * Requires: data/game-tables-raw.json (22 MB, from UE4 pak extraction)
 */

const path = require('path');
const fs = require('fs');

const TEMPLATE_PATH = path.join(__dirname, '..', '..', 'data', 'humanitz-template.db');
const RAW_JSON_PATH = path.join(__dirname, '..', '..', 'data', 'game-tables-raw.json');

// Verify raw JSON exists (needed for extraction)
if (!fs.existsSync(RAW_JSON_PATH)) {
  console.error('[build-template] ERROR: data/game-tables-raw.json not found.');
  console.error('  This file is required to extract game data for the template DB.');
  console.error('  It is generated by the UE4 pak extraction tools in tools/.');
  process.exit(1);
}

// Remove old template if it exists
if (fs.existsSync(TEMPLATE_PATH)) {
  fs.unlinkSync(TEMPLATE_PATH);
  console.log('[build-template] Removed existing template DB');
}

// Create fresh DB with schema + seed all game reference data
const HumanitZDB = require('./database');
const gameReference = require('../parsers/game-reference');

const db = new HumanitZDB({ dbPath: TEMPLATE_PATH, label: 'TEMPLATE' });
db.init();
gameReference.seed(db);

// Stamp the template with build metadata
db.setMeta('template_built_at', new Date().toISOString());
db.setMeta('template_schema_version', String(require('./schema').SCHEMA_VERSION));

// Verify row counts
const counts = {
  game_items: db.db.prepare('SELECT COUNT(*) as n FROM game_items').get().n,
  game_buildings: db.db.prepare('SELECT COUNT(*) as n FROM game_buildings').get().n,
  game_recipes: db.db.prepare('SELECT COUNT(*) as n FROM game_recipes').get().n,
  game_loot_pools: db.db.prepare('SELECT COUNT(*) as n FROM game_loot_pools').get().n,
  game_loot_pool_items: db.db.prepare('SELECT COUNT(*) as n FROM game_loot_pool_items').get().n,
  game_vehicles_ref: db.db.prepare('SELECT COUNT(*) as n FROM game_vehicles_ref').get().n,
  game_professions: db.db.prepare('SELECT COUNT(*) as n FROM game_professions').get().n,
  game_afflictions: db.db.prepare('SELECT COUNT(*) as n FROM game_afflictions').get().n,
  game_skills: db.db.prepare('SELECT COUNT(*) as n FROM game_skills').get().n,
  game_challenges: db.db.prepare('SELECT COUNT(*) as n FROM game_challenges').get().n,
  game_loading_tips: db.db.prepare('SELECT COUNT(*) as n FROM game_loading_tips').get().n,
  game_server_setting_defs: db.db.prepare('SELECT COUNT(*) as n FROM game_server_setting_defs').get().n,
  game_car_upgrades: db.db.prepare('SELECT COUNT(*) as n FROM game_car_upgrades').get().n,
  game_ammo_types: db.db.prepare('SELECT COUNT(*) as n FROM game_ammo_types').get().n,
  game_repair_data: db.db.prepare('SELECT COUNT(*) as n FROM game_repair_data').get().n,
  game_furniture: db.db.prepare('SELECT COUNT(*) as n FROM game_furniture').get().n,
  game_traps: db.db.prepare('SELECT COUNT(*) as n FROM game_traps').get().n,
  game_sprays: db.db.prepare('SELECT COUNT(*) as n FROM game_sprays').get().n,
  game_animals: db.db.prepare('SELECT COUNT(*) as n FROM game_animals').get().n,
  game_crops: db.db.prepare('SELECT COUNT(*) as n FROM game_crops').get().n,
  game_lore: db.db.prepare('SELECT COUNT(*) as n FROM game_lore').get().n,
  game_quests: db.db.prepare('SELECT COUNT(*) as n FROM game_quests').get().n,
  game_spawn_locations: db.db.prepare('SELECT COUNT(*) as n FROM game_spawn_locations').get().n,
};

db.close();

// VACUUM to minimize file size
const Database = require('better-sqlite3');
const vacDb = new Database(TEMPLATE_PATH);
vacDb.exec('VACUUM');
vacDb.close();

const sizeBytes = fs.statSync(TEMPLATE_PATH).size;
const sizeMB = (sizeBytes / 1024 / 1024).toFixed(2);
const totalRows = Object.values(counts).reduce((a, b) => a + b, 0);

console.log('');
console.log('╔══════════════════════════════════════════════════════╗');
console.log('║  Template DB built successfully                     ║');
console.log('╠══════════════════════════════════════════════════════╣');
console.log(`║  Path: data/humanitz-template.db                    ║`);
console.log(`║  Size: ${sizeMB.padStart(6)} MB (vs 22 MB game-tables-raw.json) ║`);
console.log(`║  Tables: ${String(Object.keys(counts).length).padStart(2)} game reference tables              ║`);
console.log(`║  Rows: ${String(totalRows).padStart(5)} total                              ║`);
console.log('╠══════════════════════════════════════════════════════╣');

for (const [table, count] of Object.entries(counts)) {
  const name = table.replace('game_', '').padEnd(22);
  console.log(`║  ${name} ${String(count).padStart(5)} rows                    ║`);
}

console.log('╚══════════════════════════════════════════════════════╝');
